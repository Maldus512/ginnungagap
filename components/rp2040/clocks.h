#ifndef RP2040_CLOCKS_H_INCLUDED
#define RP2040_CLOCKS_H_INCLUDED


#include "_common.h"


#define RP2040_CLOCKS_BASE          (0x40008000)
#define RP2040_CLOCKS_REF_CTRL      MMIO32(RP2040_CLOCKS_BASE + 0x30)
#define RP2040_CLOCKS_SYS_CTRL      MMIO32(RP2040_CLOCKS_BASE + 0x3C)
#define RP2040_CLOCKS_CLK_PERI_CTRL MMIO32(RP2040_CLOCKS_BASE + 0x48)
#define RP2040_CLOCKS_RESUS_CTRL    MMIO32(RP2040_CLOCKS_BASE + 0x78)


#define RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_BITS(Value) MASKSHIFT((Value), 0x3, 5)
#define RP2040_CLOCKS_CLK_PERI_CTRL_ENABLE_BIT         (1 << 11)


typedef enum {
    RP2040_CLOCKS_REF_CTRL_SCR_ROSC_CLKSRC_PH     = 0x00,
    RP2040_CLOCKS_REF_CTRL_SCR_CLKSRC_CLK_REF_AUX = 0x01,
    RP2040_CLOCKS_REF_CTRL_SCR_XOSC_CLKSRC        = 0x02,
} rp2040_clocks_ref_ctrl_src_t;


typedef enum {
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_CLK_SYS        = 0x0,
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_CLKSRC_PLL_SYS = 0x1,
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_CLKSRC_PLL_USB = 0x2,
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_ROSC_CLKSRC_PH = 0x3,
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_XOSC_CLKSRC    = 0x4,
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_CLKSRC_GPIN0   = 0x5,
    RP2040_CLOCKS_CLK_PERI_CTRL_AUXSRC_CLKSRC_GPIN1   = 0x6,
} rp2040_clocks_clk_peri_ctrl_auxsrc_t;


#endif
